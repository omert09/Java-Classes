package com.class13;

public class StringManipulationNewTopic {

	public static void main(String [] args) {
		
		String str="Java is getting interesting";
		
		System.out.println(str.substring(10)); //this method returns new string that is a substring of this string
		System.out.println(str.substring(8, 15)); //this method returns new substring in the range given. 
		
		//if it is substring 10, it includes that 10th character and continues forward. 
		
		
	}
}
