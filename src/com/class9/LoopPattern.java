package com.class9;

public class LoopPattern {

	public static void main(String [] args) {
		
		for (int i=1; i<=5; i++) {
		 for (int j=1; j<=7; j++) {
			 
			 System.out.print("*");
		 }
		 System.out.println();
		}
		for (int i=0; i<10; i++) {
			 for (int j=0; j<10; j++) {
				 
				 System.out.print("*");
			 }
			 System.out.println();
}
}
	} 